package thomas.nill.testdaten.basis;

public class TestdatenException extends RuntimeException{

	public TestdatenException() {
		super();
	}

	public TestdatenException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TestdatenException(String message, Throwable cause) {
		super(message, cause);
	}

	public TestdatenException(String message) {
		super(message);
	}

	public TestdatenException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = -9195601128824267758L;

}
